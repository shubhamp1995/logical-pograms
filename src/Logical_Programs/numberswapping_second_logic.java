

package Logical_Programs;

public class numberswapping_second_logic
{
public static void main(String[] args) {
	int a=50;
	int b=100;
	System.out.println("before swapping"+" a="+a+" "+" b="+b);
	int c=a;
	a=b;//100
	b=c;//50
	System.out.println("after swapping"+" a="+a+" "+" b="+b);
}	

}
